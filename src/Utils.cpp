wchar_t* coco_string_create(const char* str)
{
    const int len = str ? strlen(str) : 0;
    wchar_t* dest = new wchar_t[len + 1];
    for (int i = 0; i < len; ++i)
    {
        dest[i] = wchar_t(str[i]);
    }
    dest[len] = 0;
    return dest;
}


